SELECT numero_reclamo, COUNT(*) AS total,
       COUNT(DISTINCT id_jugador) AS distinct_jugadores,
       COUNT(DISTINCT tipo_reclamo) as distinct_reclamo,
       COUNT(DISTINCT fecha_reclamo_pagado) AS distinct_fecha,
       COUNT(DISTINCT indicador_anualidad) as distinct_anualidad,
       COUNT(DISTINCT indicador_anonimo) AS distinct_anonimo,
       COUNT(DISTINCT numero_comerciante) as distinct_comerciante,
       COUNT(DISTINCT ubicacion_centro_reclamo) AS distinct_centro
FROM limpieza_datos.loteria
GROUP BY numero_reclamo
HAVING COUNT(DISTINCT id_jugador) > 1
    OR COUNT(DISTINCT tipo_reclamo) > 1
    OR COUNT(DISTINCT fecha_reclamo_pagado) > 1
    OR COUNT(DISTINCT indicador_anualidad) > 1
    OR COUNT(DISTINCT indicador_anonimo) > 1
    OR COUNT(DISTINCT numero_comerciante) > 1
    OR COUNT(DISTINCT ubicacion_centro_reclamo)  > 1
ORDER BY COUNT(DISTINCT id_jugador) DESC;

